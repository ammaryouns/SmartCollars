/**
* @brief This function handles Hard fault interrupt.
*/
void hard_fault_handler_c(unsigned int * hardfault_args, unsigned int r4, unsigned int r5, unsigned int r6)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
  LOG_Error(LOG_ErrorSource_SecondarySystem, LOG_Error_HardFaultPC, hardfault_args[6]);
  LOG_Error(LOG_ErrorSource_SecondarySystem, LOG_Error_HardFaultLR, hardfault_args[5]);
  LOG_Error(LOG_ErrorSource_SecondarySystem, LOG_Error_HardFaultSP, hardfault_args[8]);
        
  
  printTask ("[Hard Fault]\n"); // After Joseph Yiu

  printTask ("r0 = %08X, r1 = %08X, r2 = %08X, r3 = %08X\n",
    hardfault_args[0], hardfault_args[1], hardfault_args[2], hardfault_args[3]);
  printTask ("r4 = %08X, r5 = %08X, r6 = %08X, sp = %08X\n",
    r4, r5, r6, (unsigned int)&hardfault_args[8]);
  printTask ("r12= %08X, lr = %08X, pc = %08X, psr= %08X\n",
    hardfault_args[4], hardfault_args[5], hardfault_args[6], hardfault_args[7]);

  printTask ("bfar=%08X, cfsr=%08X, hfsr=%08X, dfsr=%08X, afsr=%08X\n",
    *((volatile unsigned int *)(0xE000ED38)),
    *((volatile unsigned int *)(0xE000ED28)),
    *((volatile unsigned int *)(0xE000ED2C)),
    *((volatile unsigned int *)(0xE000ED30)),
    *((volatile unsigned int *)(0xE000ED3C)) );

  printTask("HAL_NVIC_SystemReset\n");
    HAL_NVIC_SystemReset();
  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
  /* USER CODE BEGIN HardFault_IRQn 1 */

  /* USER CODE END HardFault_IRQn 1 */
}

